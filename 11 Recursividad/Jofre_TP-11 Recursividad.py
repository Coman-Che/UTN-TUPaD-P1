# 5) Implement√° una funci√≥n recursiva llamada es_palindromo(palabra) que reciba una
# cadena de texto sin espacios ni tildes, y devuelva True si es un pal√≠ndromo o False si no
# lo es.
# Requisitos:
# La soluci√≥n debe ser recursiva.
# No se debe usar [::-1] ni la funci√≥n reversed().

def es_palindromo(palabra):
    # Caso base: si la palabra tiene 0 o 1 letras, es un pal√≠ndromo
    if len(palabra) <= 1:
        return True
    # Comparar primer y √∫ltimo car√°cter
    if palabra[0] != palabra[-1]:
        return False
    # Llamada recursiva con la subcadena del medio
    return es_palindromo(palabra[1:-1])

# Prueba del usuario
entrada = input("Introduce una palabra (sin espacios ni tildes): ").lower()

# Mostrar el resultado
if es_palindromo(entrada):
    print(f"'{entrada}' es un pal√≠ndromo.")
else:
    print(f"'{entrada}' no es un pal√≠ndromo.")

