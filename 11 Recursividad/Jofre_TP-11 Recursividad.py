# 4) Crear una funci√≥n recursiva en Python que reciba un n√∫mero entero positivo en base
# decimal y devuelva su representaci√≥n en binario como una cadena de texto.
# Cuando representamos un n√∫mero en binario, lo expresamos usando solamente ceros (0) y
# unos (1), en base 2. Para convertir un n√∫mero decimal a binario, se puede seguir este
# procedimiento:
# 1. Dividir el n√∫mero por 2.
# 2. Guardar el resto (0 o 1).
# 3. Repetir el proceso con el cociente hasta que llegue a 0.
# 4. Los restos obtenidos, le√≠dos de abajo hacia arriba, forman el n√∫mero binario.
# Convertir el n√∫mero 10 a binario:
# 10 √∑ 2 = 5 resto: 0
# 5 √∑ 2 = 2 resto: 1
# 2 √∑ 2 = 1 resto: 0
# 1 √∑ 2 = 0 resto: 1
# Leyendo los restos de abajo hacia arriba: 1 0 1 0 ‚Üí El resultado binario es "1010".

# Funci√≥n recursiva para convertir un n√∫mero decimal a binario
def decimal_a_binario(n):
    if n == 0:
        return ''
    else:
        return decimal_a_binario(n // 2) + str(n % 2)

# Solicitar un n√∫mero al usuario
numero = int(input("Introduce un n√∫mero entero positivo: "))

if numero < 0:
    print("Por favor, introduce un n√∫mero entero positivo.")
elif numero == 0:
    print("El n√∫mero 0 en binario es: 0")
else:
    binario = decimal_a_binario(numero)
    print(f"El n√∫mero {numero} en binario es: {binario}")

