# 3) Crea una funci√≥n recursiva que calcule la potencia de un n√∫mero base elevado a un
# exponente, utilizando la f√≥rmula ùëõùëö = ùëõ ‚àó ùëõ(ùëö‚àí1). Prueba esta funci√≥n en un
# algoritmo general.

# Funci√≥n recursiva para calcular la potencia
def potencia(base, exponente):
    if exponente == 0:
        return 1
    else:
        return base * potencia(base, exponente - 1)

# Solicitar al usuario la base y el exponente
base = float(input("Introduce la base (n√∫mero): "))
exponente = int(input("Introduce el exponente (entero no negativo): "))

# Verificaci√≥n del exponente
if exponente < 0:
    print("Por favor, introduce un exponente entero no negativo.")
else:
    resultado = potencia(base, exponente)
    print(f"{base} elevado a la {exponente} es igual a {resultado}")

