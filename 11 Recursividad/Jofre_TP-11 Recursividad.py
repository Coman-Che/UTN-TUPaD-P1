# 8) Escrib√≠ una funci√≥n recursiva llamada contar_digito(numero, digito) que reciba un
# n√∫mero entero positivo (numero) y un d√≠gito (entre 0 y 9), y devuelva cu√°ntas veces
# aparece ese d√≠gito dentro del n√∫mero.
# Ejemplos:
# contar_digito(12233421, 2) ‚Üí 3
# contar_digito(5555, 5) ‚Üí 4
# contar_digito(123456, 7) ‚Üí 0

def contar_digito(numero, digito):
    if numero == 0:
        return 0
    else:
        coincidencia = 1 if (numero % 10) == digito else 0
        return coincidencia + contar_digito(numero // 10, digito)

# Ejemplo de uso
numero = int(input("Introduce un n√∫mero entero positivo: "))
digito = int(input("Introduce un d√≠gito entre 0 y 9: "))

# Validaci√≥n de entradas
if numero < 0 or digito < 0 or digito > 9:
    print("Entrada inv√°lida. Aseg√∫rate de ingresar un n√∫mero positivo y un d√≠gito entre 0 y 9.")
else:
    resultado = contar_digito(numero, digito)
    print(f"El d√≠gito {digito} aparece {resultado} veces en el n√∫mero {numero}.")