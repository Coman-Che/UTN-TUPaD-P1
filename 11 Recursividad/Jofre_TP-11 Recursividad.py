# 2) Crea una funci√≥n recursiva que calcule el valor de la serie de Fibonacci en la posici√≥n
# indicada. Posteriormente, muestra la serie completa hasta la posici√≥n que el usuario
# especifique.

# Funci√≥n recursiva para calcular el valor de Fibonacci en la posici√≥n n
def fibonacci(n):
    if n == 0:
        return 0
    elif n == 1:
        return 1
    else:
        return fibonacci(n - 1) + fibonacci(n - 2)

# Solicitar al usuario una posici√≥n
posicion = int(input("Introduce una posici√≥n (n√∫mero entero no negativo): "))

# Verificamos que el n√∫mero sea v√°lido
if posicion < 0:
    print("Por favor, introduce un n√∫mero entero no negativo.")
else:
    print(f"Serie de Fibonacci hasta la posici√≥n {posicion}:")
    for i in range(posicion + 1):
        print(f"F({i}) = {fibonacci(i)}")

