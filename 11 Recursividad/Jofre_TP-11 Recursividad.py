# 1) Crea una funci√≥n recursiva que calcule el factorial de un n√∫mero. Luego, utiliza esa
# funci√≥n para calcular y mostrar en pantalla el factorial de todos los n√∫meros enteros
# entre 1 y el n√∫mero que indique el usuario

# Funci√≥n recursiva para calcular el factorial
def factorial(n):
    if n == 0 or n == 1:
        return 1
    else:
        return n * factorial(n - 1)

# Pedir al usuario un n√∫mero entero positivo
numero = int(input("Introduce un n√∫mero entero positivo: "))

# Verificamos que el n√∫mero sea mayor o igual a 1
if numero < 1:
    print("Por favor, introduce un n√∫mero mayor o igual a 1.")
else:
    print(f"Factoriales del 1 al {numero}:")
    for i in range(1, numero + 1):
        print(f"{i}! = {factorial(i)}")

